import re

text = 'А578ВЕ777 ОР233787 К901МН666 СТ46599 СНИ2929П777 666АМР666'

auto = re.findall(r'[АВЕКМНОРСТУХ]\d{3}[АВЕКМНОРСТУХ]{2}\d{2,3}',text)
print(f"Список номеров частных автомобилей: {auto}")

taxi = re.findall(r'[АВЕКМНОРСТУХ]{2}\d{3}\d{2,3}',text)
print(f"Список номеров частных автомобилей: {taxi}")